//! Raw HID protocol for the MSI MAG Z890 Tomahawk (Mystic Light). RGB-fan
//! control via the JARGB_V2 headers. The Mystic Light controller enumerates
//! under MSI's *other* USB VID 0x0DB0 (PID 0x0076), as a single vendor-defined
//! collection (usage page 0xFF00) whose serial carries the board number
//! (7E32...). Note: 0x1462/0x7E32 is the board *model* (MS-7E32), not a USB id.
//!
//! Derived from the OpenRGB `MSIMysticLight761Controller` (GPLv2, read as
//! protocol documentation only — never linked/spawned). Everything is HID
//! FEATURE reports; there are no interrupt/output writes:
//! - **Enable direct mode** once: a 290-byte report id 0x50 (`SETUP`). The
//!   JARGB/JAF rows (setup indices 0..3) are active in the template, so sending
//!   it verbatim arms the JARGB_V2 headers.
//! - **Per-LED frame:** one 727-byte report id 0x51 per header:
//!   `[51 09 hdr0 hdr1 00 00 led_count] + colors[720]`, where hdr0 = 0x04 for
//!   JARGB, hdr1 = the header index (0,1,2). Wire color order is RGB.

use hidapi::{HidApi, HidDevice};

use crate::device::types::Color;
use crate::device::DeviceError;

pub const VID: u16 = 0x0DB0;
pub const PID: u16 = 0x0076;

/// HID usage page of the Mystic Light control collection (vendor-defined).
const USAGE_PAGE: u16 = 0xFF00;

/// Header byte hdr0 selecting an addressable (JARGB) header.
const HDR0_JARGB: u8 = 0x04;

/// Max LEDs the per-LED packet can carry (colors[720] = 240 * 3).
pub const MAX_LEDS: usize = 240;

/// Bytes per per-LED packet: 7-byte header + 720-byte color array.
const PACKET_LEN: usize = 7 + 240 * 3;

/// Direct-mode setup report (id 0x50). Verbatim OpenRGB `initial_setup_array`:
/// 0x50 + 18 zone rows of 16 bytes + trailing 0x00 = 290 bytes. JARGB/JAF rows
/// (the first four) are already active (`..03 15 78`); the rest are the
/// template's pipe/rgb/onboard rows. We drive only JARGB, so this verbatim
/// template is sufficient to arm the fan headers.
#[rustfmt::skip]
const SETUP: [u8; 290] = [
    0x50,
    0x09,0xFF,0x00,0x00,0x00,0xFF,0x00,0x00,0x00,0xFF,0xFF,0xFF,0xFF,0x03,0x15,0x78, // JARGB 1
    0x09,0xFF,0x00,0x00,0x00,0xFF,0x00,0x00,0x00,0xFF,0xFF,0xFF,0xFF,0x03,0x15,0x78, // JARGB 2
    0x09,0xFF,0x00,0x00,0x00,0xFF,0x00,0x00,0x00,0xFF,0xFF,0xFF,0xFF,0x03,0x15,0x78, // JARGB 3
    0x09,0xFF,0x00,0x00,0x00,0xFF,0x00,0x00,0x00,0xFF,0xFF,0xFF,0xFF,0x03,0x15,0x78, // JAF
    0x09,0xFF,0x00,0x00,0x00,0xFF,0x00,0x00,0x00,0xFF,0xFF,0xFF,0xFF,0x03,0x95,0x1E, // JPIPE1
    0x09,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x94,0x1E, // JPIPE2
    0x09,0xFF,0x00,0x00,0x00,0xFF,0x00,0x00,0x00,0xFF,0xFF,0xFF,0xFF,0x03,0x95,0x1E, // JPIPE3
    0x09,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x94,0x1E, // JPIPE4
    0x09,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x94,0x1E, // JPIPE5
    0x09,0xFF,0x00,0x00,0x00,0xFF,0x00,0x00,0x00,0xFF,0xFF,0xFF,0xFF,0x03,0x95,0x1E, // JRGB1
    0x09,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x94,0x1E, // JRGB2
    0x09,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x94,0x1E, // Onboard1
    0x09,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x94,0x1E, // Onboard2
    0x09,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x94,0x1E, // Onboard3
    0x09,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x94,0x1E, // Onboard4
    0x09,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x94,0x1E, // Onboard5
    0x09,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x94,0x1E, // Onboard6
    0x09,0xFF,0x00,0x00,0x00,0xFF,0x00,0x00,0x00,0xFF,0xFF,0xFF,0xFF,0x03,0x95,0x1E, // Select all
    0x00,
];

fn comm(e: impl std::fmt::Display) -> DeviceError {
    DeviceError::Comm(e.to_string())
}

pub struct MsiController {
    dev: HidDevice,
}

impl MsiController {
    /// Open the Mystic Light control collection. Prefers the device reporting
    /// usage page 0x0001, falling back to the first VID/PID match. Ok(None) =
    /// board not present.
    pub fn open() -> Result<Option<Self>, DeviceError> {
        let api = HidApi::new().map_err(comm)?;
        let msi = |d: &&hidapi::DeviceInfo| d.vendor_id() == VID && d.product_id() == PID;
        let Some(info) = api
            .device_list()
            .find(|d| msi(d) && d.usage_page() == USAGE_PAGE)
            .or_else(|| api.device_list().find(msi))
        else {
            return Ok(None);
        };
        let dev = api.open_path(info.path()).map_err(comm)?;
        Ok(Some(MsiController { dev }))
    }

    /// Send the 0x50 direct-mode setup report. Arms the JARGB headers; call once
    /// on probe and again after any frame write fails (the board can drop direct
    /// mode on error).
    pub fn send_setup(&self) -> Result<(), DeviceError> {
        self.dev.send_feature_report(&SETUP).map_err(comm)
    }

    /// Stream one header's per-LED colors (report id 0x51). `hdr1` is the header
    /// index (0 = JARGB_V2_1, 1 = JARGB_V2_2, 2 = JARGB_V2_3). Colors beyond MAX_LEDS are
    /// dropped; the unused tail stays black. Wire order is RGB.
    pub fn set_header(&self, hdr1: u8, colors: &[Color]) -> Result<(), DeviceError> {
        let n = colors.len().min(MAX_LEDS);
        let mut buf = [0u8; PACKET_LEN];
        buf[0] = 0x51; // report id
        buf[1] = 0x09; // fixed
        buf[2] = HDR0_JARGB;
        buf[3] = hdr1;
        // buf[4], buf[5] fixed 0x00
        buf[6] = n as u8; // led count
        for (i, c) in colors[..n].iter().enumerate() {
            let o = 7 + i * 3;
            buf[o] = c.r;
            buf[o + 1] = c.g;
            buf[o + 2] = c.b;
        }
        self.dev.send_feature_report(&buf).map_err(comm)
    }
}
