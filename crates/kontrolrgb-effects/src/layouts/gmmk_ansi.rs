pub fn full_size() -> Vec<KeyInfo> {
    KEYS.iter()
        .enumerate()
        .map(|(i, k)| KeyInfo {
            led_index: i as u32,
            label: k.0.to_string(),
            x: k.1,
            y: k.2,
            w: k.3,
            h: k.4,
        })
        .collect()
}
